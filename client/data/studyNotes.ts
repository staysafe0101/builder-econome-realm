// Generate additional study notes programmatically
export const generateAdditionalStudyNotes = (): StudyNote[] => {
  const topics = [
    "Advanced Banking",
    "Investment Strategies",
    "Business Finance",
    "Real Estate Basics",
    "Economic Principles",
    "Financial Technology",
    "Risk Management",
    "Money Psychology",
    "Global Finance",
    "Career Finance",
    "Debt Management",
    "Financial Planning",
    "Money History",
    "Financial Math",
    "Consumer Rights",
    "Estate Planning",
    "Tax Strategies",
    "Financial Markets",
    "Money and Relationships",
    "Financial Independence",
    "Green Finance",
    "Financial Literacy",
    "Money Management",
    "Investment Basics",
    "Personal Banking",
    "Credit Management",
    "Savings Strategies",
    "Financial Security",
    "Money Skills",
    "Financial Success",
  ];

  const additionalNotes: StudyNote[] = [];
  const icons = [
    "TrendingUp",
    "DollarSign",
    "PiggyBank",
    "CreditCard",
    "Building",
    "Calculator",
    "Shield",
    "Target",
    "BookOpen",
    "Lightbulb",
  ];
  const colors = ["econome-green", "econome-blue", "econome-yellow"];

  for (let i = 21; i <= 50; i++) {
    const topic = topics[i - 21];
    const difficulty =
      i <= 30 ? "Beginner" : i <= 40 ? "Intermediate" : "Advanced";
    const icon = icons[(i - 21) % icons.length];
    const color = colors[(i - 21) % colors.length];

    additionalNotes.push({
      id: i,
      title: topic,
      category: topic.split(" ")[0],
      difficulty,
      icon,
      color,
      content: [
        `📚 **Introduction:** Understanding ${topic.toLowerCase()} fundamentals`,
        `🎯 **Key Concept:** Essential principles of ${topic.toLowerCase()}`,
        `💡 **Pro Tips:** Expert strategies for ${topic.toLowerCase()}`,
        `📊 **Practical Application:** How to use ${topic.toLowerCase()} in real life`,
        `🚀 **Next Steps:** Advanced techniques in ${topic.toLowerCase()}`,
      ],
      downloadSize: `${(Math.random() * 2 + 1.5).toFixed(1)} MB`,
      estimatedReadTime: `${Math.floor(Math.random() * 5 + 4)} min`,
    });
  }

  return additionalNotes;
};

export const getAllStudyNotes = (): StudyNote[] => {
  return [...studyNotes, ...generateAdditionalStudyNotes()];
};
